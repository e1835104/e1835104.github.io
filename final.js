function English(language) {
    var message = "";

    if (language == 1) {
        message = "不正解";
    } else if (language == 2) {
        message = "不正解";
    } else if (language == 3) {
        message = "正解";
    } else  {
        message = "想像されていない引数です。";
    }

    alert(message);

}

